	DELETE FROM AnimationShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM AnimeShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM AnthologyShows WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM ComedyShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM CrimeShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM DocumentaryShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM DramaShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM FamilyShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM FantasyMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM HistoricalDramaShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM HistoryShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM HorrorShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM KidsShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM MedicalDramaShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM MysteryShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM RomanceShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM ScifiShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
