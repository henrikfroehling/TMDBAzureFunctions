	DELETE FROM TeenDramaShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM ThrillerShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM TrendingShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM WarShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM WesternShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM WorkplaceComedyShowsAndMovies WHERE ListItemId IN (SELECT ID FROM DELETED)
	DELETE FROM ListItems WHERE ID IN (SELECT ID FROM DELETED) /* necessary, because trigger is an INSTEAD OF trigger */
end;
